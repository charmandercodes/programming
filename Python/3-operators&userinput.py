# Works with strings as welll

# x = "Hello"
# y = 'Hi'
# y = "Hello"

print(x == y)
print(x < y)
print(x > y)
print(x >= y)
print(x <= y)
print(x != y)


# LOGICAL OPERATORS 

# And: Both sides have to true to be true

print(10 < 20 and 5 > 20)
  
# Or: One side has to be true to be true 

print(10 < 20 or 5 > 20)

# Not: Negates whatever truth value is 

print(not (10 < 20 and 5 > 20))


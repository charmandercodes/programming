# ARITHMETIC OPERATORS 

# You have addition, subtraction, multiplication , division

print(2 + 2)
print(2 - 2)
print(2 * 2)
print(2 / 2)

# Modulus gives you remainder of a number

print(12 % 3)
print(11 % 3)

# Floor division rounds down the number 

print( 10 // 3)
print( -10 // 3)

# Exponent which is power 

print(2**4)

